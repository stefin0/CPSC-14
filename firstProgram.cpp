#include <iostream>
using namespace std;

int main() {
    // declare and initialize variables
    int number1 = 1;
    int number2 = 2;
    double number3 = 3;

    cout << "Adding 1 + 2 = " << number1 + number2 << endl;
    cout << "Subtracting 2 - 3 = " << number2 - number3 << endl;
    cout << "Multiplying 3 * 1 = " << number3 * number1 << endl;
    cout << "Dividing 1 / 3 = " << number1 / number3 << endl;

    return 0;
}